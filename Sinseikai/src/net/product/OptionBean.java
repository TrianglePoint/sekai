package net.product;

public class OptionBean {
	private int productNumber;
	private String majorName;
	private int majiorNumber;
	private int minorNumber;
	private String minorName;
	private String minorImg;
	private int minorPrice;
	private int minorStock;
	public int getProductNumber() {
		return productNumber;
	}
	public void setProductNumber(int productNumber) {
		this.productNumber = productNumber;
	}
	public String getMajorName() {
		return majorName;
	}
	public void setMajorName(String majorName) {
		this.majorName = majorName;
	}
	public int getMajiorNumber() {
		return majiorNumber;
	}
	public void setMajiorNumber(int majiorNumber) {
		this.majiorNumber = majiorNumber;
	}
	public int getMinorNumber() {
		return minorNumber;
	}
	public void setMinorNumber(int minorNumber) {
		this.minorNumber = minorNumber;
	}
	public String getMinorName() {
		return minorName;
	}
	public void setMinorName(String minorName) {
		this.minorName = minorName;
	}
	public String getMinorImg() {
		return minorImg;
	}
	public void setMinorImg(String minorImg) {
		this.minorImg = minorImg;
	}
	public int getMinorPrice() {
		return minorPrice;
	}
	public void setMinorPrice(int minorPrice) {
		this.minorPrice = minorPrice;
	}
	public int getMinorStock() {
		return minorStock;
	}
	public void setMinorStock(int minorStock) {
		this.minorStock = minorStock;
	}
	
}
